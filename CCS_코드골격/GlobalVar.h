#ifndef GLOBALVAR_H_
#define GLOBALVAR_H_

#include <math.h>

#define PI_F        3.14159265358979f
#define TWO_PI      6.28318530717959f
#define SQRT3       1.73205080756888f

//------------------------------------------------------------------
// 제어 주기 (자료 01: 인터럽트 / 자료 02: ePWM 으로 만든 박자)
//------------------------------------------------------------------
#define PWM_CLK     200000000.0f   // ePWM 시스템 클록 200MHz (자료02 TBCLK 계산)
#define FSAMP       20000.0f       // 제어/샘플링 주파수 20kHz
#define TSAMP       (1.0f/FSAMP)   // 제어 주기 [s] = 50us

//------------------------------------------------------------------
// 모터 파라미터 (자료 04). ※ 실제 모터 데이터시트 값으로 교체할 것
//------------------------------------------------------------------
#define POLE_PAIRS  4.0f           // 극쌍수 P
#define RS          0.30f          // 상저항 [Ohm]
#define LD          0.0002f        // d축 인덕턴스 [H]
#define LQ          0.0002f        // q축 인덕턴스 [H]
#define LAMBDA_PM   0.0064f        // 영구자석 쇄교자속 [Wb]
#define VDC_NOM     24.0f          // DC 링크 공칭 전압 [V]
#define VMAX        (VDC_NOM/SQRT3)// 선형변조 전압 한계 (자료04 변조한계)

//------------------------------------------------------------------
// 제어 모드 (자료 04: 제어 4단계)
//------------------------------------------------------------------
typedef enum { MODE_VF=0, MODE_IF=1, MODE_VECTOR=2, MODE_SPEED=3 } CtrlMode;

//------------------------------------------------------------------
// PI 제어기 구조체 (자료 04)
//------------------------------------------------------------------
typedef struct {
    float Kp;       // 비례 이득
    float Ki;       // 적분 이득
    float integ;    // 적분 누적값
    float out_max;  // 출력 포화 한계 (와인드업 방지)
} PI_t;

//------------------------------------------------------------------
// 전역 상태/지령 (CCS Expression 창에서 관찰·수정하는 변수들)
//------------------------------------------------------------------
typedef struct {
    CtrlMode mode;

    // --- 지령 (사용자가 바꾸는 값) ---
    float speed_ref_rpm;   // 속도 지령
    float id_ref;          // d축 전류 지령 (보통 0)
    float iq_ref;          // q축 전류 지령 (VECTOR 모드)

    // --- 측정 ---
    float ia, ib, ic;      // 상전류 (ADC→스케일, 자료03)
    float theta_e;         // 회전자 전기각 [rad] (엔코더)
    float wm;              // 기계 각속도 [rad/s]

    // --- 중간 계산값 (디버깅용 관찰) ---
    float ialpha, ibeta;   // Clarke
    float id, iq;          // Park
    float vd, vq;          // PI 출력
    float valpha, vbeta;   // inv Park
    float duty_a, duty_b, duty_c;  // 최종 듀티 (자료02 → CMPA)

    // --- 제어기 ---
    PI_t pi_id, pi_iq, pi_speed;

    // --- V/F, I/F 용 명령각 ---
    float theta_c;
} FocState;

extern FocState g;             // 전역 인스턴스
extern volatile Uint16 uMaxCountSamp;  // ePWM TBPRD (자료02)

#endif /* GLOBALVAR_H_ */
